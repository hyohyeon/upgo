package com.upgo.dto;

import java.util.ArrayList;
import java.util.Date;

public class OrderProduct {
	
	private int OrdNo;
	private String  OrdProducts;
	private int  OrdPrices;
	private Date PrdRegDate;
	private boolean PrdDeleted;
	
	
	public int getOrdNo() {
		return OrdNo;
	}
	public void setOrdNo(int ordNo) {
		OrdNo = ordNo;
	}
	public String getOrdProducts() {
		return OrdProducts;
	}
	public void setOrdProducts(String ordProducts) {
		OrdProducts = ordProducts;
	}
	public int getOrdPrices() {
		return OrdPrices;
	}
	public void setOrdPrices(int ordPrices) {
		OrdPrices = ordPrices;
	}
	public Date getPrdRegDate() {
		return PrdRegDate;
	}
	public void setPrdRegDate(Date prdRegDate) {
		PrdRegDate = prdRegDate;
	}
	public boolean isPrdDeleted() {
		return PrdDeleted;
	}
	public void setPrdDeleted(boolean prdDeleted) {
		PrdDeleted = prdDeleted;
	}
	
	
}
	